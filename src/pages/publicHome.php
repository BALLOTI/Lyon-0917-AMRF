        <h3>Le Wiki des Maires : qu’est-ce que c’est ? </h3>
        <p>
            A destination des adhérents de l’Association des Maires Ruraux de France et de ses partenaires, le Wiki des
            Maires permet de partager et d’échanger sur des projets mis en place par les communes rurales adhérentes.
            Plateforme interactive et évolutive, elle illustre le dynamisme de nos communes et l’implication des élus
            sur leur territoire et pour leurs administrés. Nous ne le dirons jamais assez, l’échelon communal permet
            l’innovation et la réalisation de projets divers que ce Wiki permet de partager.
            Alors racontez-nous !
        </p>
