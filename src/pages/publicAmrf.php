<h2>À propos de l'AMRF</h2>

<h3>L'ASSOCIATION DES MAIRES RURAUX DE FRANCE</h3>

<p>Créée en 1971, l'Association des maires ruraux de France (AMRF) fédère près de 10000 maires
    ruraux au sein d'un réseau solidaire, en toute indépendance des pouvoirs et partis politiques.
    Rassemblés autour d'une identité forte, les membres de l'AMRF portent la voix des communes
    ancrées sur les territoires ruraux pour défendre leurs enjeux spécifiques.
    Forte de son militantisme et de sa représentativité, l'AMRF est aujourd'hui l'interlocutrice
    incontournable des communes rurales auprès des pouvoirs publics et des grands opérateurs
    nationaux.<br/></p>

<h3>DÉFENDRE LES ENJEUX DE LA RURALITÉ</h3>

<p>Dans un esprit militant, l'AMRF fédère, informe et représente les maires des communes de moins
    de 3 500 habitants, partout en France.
    L'association s'engage au quotidien - au niveau local et national - pour défendre et promouvoir
    les enjeux spécifiques de la ruralité. Elle participe au débat public en formulant des propositions
    et en réalisant des interventions. Elle produit des manifestes et publie des rapports et des
    contributions lors des discussions parlementaires.<br/></p>

<h3>UN RÉSEAU AU SERVICE DES TERRITOIRES RURAUX</h3>

<p>Les associations départementales, en totale autonomie, représentent les maires ruraux auprès
    des interlocuteurs publics du département et services déconcentrés (Préfecture, Conseil
    Départemental, Inspection d'Académie, Gendarmerie...) et des opérateurs de services.
    Formant un réseau de proximité efficace, elles sont une force d'information et de mobilisation très
    réactive.<br/></p>



