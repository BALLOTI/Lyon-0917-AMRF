                <h4 class="card-title">Des maires militants de la ruralité</h4>
                <p class="card-text text-truncate">L'Association des Maires Ruraux de France fédère, informe et
                    représente les maires des communes de moins
                    de 3 500 habitants partout en France. L'association s'engage au quotidien au niveau local comme
                    national pour défendre et promouvoir les enjeux spécifiques de la ruralité.
                    Créée en 1971, l'AMRF rassemble près de 10 000 maires ruraux regroupés dans un réseau convivial et
                    solidaire d'associations départementales, en toute indépendance des pouvoirs et partis politiques.
                    L'AMRF est un représentant incontournable du monde rural auprès des pouvoirs publics et des grands
                    opérateurs nationaux.
                    Les 10 engagements de l'AMRF
                    Défendre la commune et la liberté municipale, expression primordiale de la démocratie ;
                    Porter les positions des élus ruraux dans les instances locales et nationales en conservant notre
                    indépendance vis-à-vis des pouvoirs et partis politiques. Représenter et soutenir les élus ruraux
                    dans leurs actions ;
                    Agir pour le maintien d’un tissu de services publics adapté aux besoins et se mobiliser pour les
                    défendre ;
                    Développer une image positive, moderne et dynamique de la ruralité ;
                    Œuvrer pour un aménagement équilibré, juste et concerté du territoire ;
                    Revendiquer l’égalité républicaine de tous les citoyens en combattant les privilèges financiers de
                    la ville sur la campagne ;
                    Agir pour un dynamisme économique et social durable en luttant contre la désertification rurale ;
                    Affirmer et faire reconnaitre la spécificité des territoires ruraux ;
                    S’opposer au cumul des mandats électifs et des fonctions, entrave majeure à la démocratie ;
                    Développer liens et coopérations avec les structures et organismes nationaux qui partagent notre
                    vision de la démocratie et des territoires. Encourager, promouvoir et soutenir toute initiative
                    allant dans ce sens.</p>
